public class Category {
    // Название категории
    private String name;

    // Конструктор
    public Category(String name) {
        this.name = name;
    }

    // Геттер для названия категории
    public String getName() {
        return name;
    }

    // Сеттер для названия категории
    public void setName(String name) {
        this.name = name;
    }
}
